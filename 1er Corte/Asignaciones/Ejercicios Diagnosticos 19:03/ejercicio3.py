#Leer el salario de un empleado y aumentarlo un 25%
#utilizamos la formula general del aumento porcentual valor inicial * (1 + Tasa de aumento)
salario = float(input("Ingrese el salario del empleado: "))
nuevo_salario = salario * 1.25
print(f"El nuevo salario del empleado con el aumento es: {nuevo_salario} ")
